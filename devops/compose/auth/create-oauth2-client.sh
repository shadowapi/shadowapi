#!/bin/bash
# Creates the OAuth2 SPA client in Hydra for the ShadowAPI frontend
# Run this after Hydra is up and migrations are complete
# Note: This script is kept for manual use. For automated setup, use devops/bootstrap.sh

set -e

REDIRECT_URI="${BE_OAUTH2_REDIRECT_URI:-http://localtest.me/api/v1/auth/oauth2/callback}"
HYDRA_ADMIN_URL="${HYDRA_ADMIN_ENDPOINT:-http://localhost:4445}"
CLIENT_NAME="ShadowAPI SPA"

echo "Creating OAuth2 client: $CLIENT_NAME"

# Check if client already exists by name
EXISTING_CLIENT=$(docker compose exec -T hydra hydra list oauth2-clients \
    --endpoint "$HYDRA_ADMIN_URL" \
    --format json 2>/dev/null | jq -r ".items[] | select(.client_name == \"$CLIENT_NAME\") | .client_id" || echo "")

if [ -n "$EXISTING_CLIENT" ]; then
    echo "Client already exists with ID: $EXISTING_CLIENT"
    echo ""
    echo "To update, delete the client first:"
    echo "docker compose exec hydra hydra delete oauth2-client $EXISTING_CLIENT --endpoint $HYDRA_ADMIN_URL"
    exit 0
fi

# Create new client (ID is auto-generated by Hydra)
echo "Creating new client..."
CLIENT_RESPONSE=$(docker compose exec -T hydra hydra create oauth2-client \
    --endpoint "$HYDRA_ADMIN_URL" \
    --name "$CLIENT_NAME" \
    --grant-type authorization_code,refresh_token \
    --response-type code \
    --scope openid,offline_access,profile,email \
    --redirect-uri "$REDIRECT_URI" \
    --token-endpoint-auth-method none \
    --format json)

CLIENT_ID=$(echo "$CLIENT_RESPONSE" | jq -r '.client_id')

echo ""
echo "OAuth2 client created successfully!"
echo "Client ID: $CLIENT_ID"
echo "Redirect URI: $REDIRECT_URI"
echo ""
echo "Add this to your .env file:"
echo "BE_OAUTH2_SPA_CLIENT_ID=$CLIENT_ID"
